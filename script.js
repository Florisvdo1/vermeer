const artworks = window.__ARTWORKS__ || [];
const artGrid = document.getElementById("art-grid");
const langButtons = [...document.querySelectorAll("[data-lang-switch]")];
const translatableNodes = [...document.querySelectorAll("[data-i18n]")];
const menuToggle = document.querySelector(".menu-toggle");
const mobilePanel = document.querySelector(".mobile-panel");

let currentLang = localStorage.getItem("oever_lang") || "nl";

function renderArtworks(lang) {
  artGrid.innerHTML = "";
  artworks.forEach((art, index) => {
    const card = document.createElement("article");
    card.className = "panel art-card";
    card.innerHTML = `
      <div class="art-visual">
        <span class="art-index">${String(index + 1).padStart(2, "0")}</span>
        <img src="${art.image}" alt="${lang === "nl" ? art.title_nl : art.title_en}" loading="lazy" />
      </div>
      <div class="art-copy">
        <div class="art-meta">${translations[lang]["art.meta"]}</div>
        <h3>${lang === "nl" ? art.title_nl : art.title_en}</h3>
        <p>${lang === "nl" ? art.desc_nl : art.desc_en}</p>
        <div class="art-actions">
          <a class="btn art-buy" href="${art.url}" target="_blank" rel="noopener noreferrer">${translations[lang]["art.buy"]}</a>
        </div>
      </div>
    `;
    artGrid.appendChild(card);
  });
}

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  localStorage.setItem("oever_lang", lang);
  langButtons.forEach(btn => btn.classList.toggle("is-active", btn.dataset.langSwitch === lang));
  translatableNodes.forEach(node => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) node.textContent = translations[lang][key];
  });
  renderArtworks(lang);
}

langButtons.forEach(btn => {
  btn.addEventListener("click", () => applyLanguage(btn.dataset.langSwitch));
});

menuToggle?.addEventListener("click", () => {
  const isOpen = !mobilePanel.hasAttribute("hidden");
  if (isOpen) {
    mobilePanel.setAttribute("hidden", "");
    menuToggle.setAttribute("aria-expanded", "false");
  } else {
    mobilePanel.removeAttribute("hidden");
    menuToggle.setAttribute("aria-expanded", "true");
  }
});

document.querySelectorAll(".mobile-nav a").forEach(link => {
  link.addEventListener("click", () => {
    mobilePanel.setAttribute("hidden", "");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

applyLanguage(currentLang);
